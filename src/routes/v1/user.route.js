const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const userValidation = require('../../validations/user.validation');
const userController = require('../../controllers/user.controller');
const logger = require('../../config/logger');

const router = express.Router();

/**
 * Log et traitement avant chaque route
 */
const logRoute = (method, path) => (req, res, next) => {
  logger.info(`[ROUTE] ${method.toUpperCase()} ${path} - UserID=${req.user ? req.user.id : 'Anonymous'}`);
  next();
};

router
  .route('/')
  .post(
    auth('manageUsers'), // Permission admin requise
    validate(userValidation.createUser),
    logRoute('POST', '/users'),
    userController.createUser
  )
  .get(
    auth('getUsers'), // Permission admin requise
    validate(userValidation.getUsers),
    logRoute('GET', '/users'),
    userController.getUsers
  );

router
  .route('/:userId')
  .get(
    auth('getUsers'), // Permission admin requise ou accès personnel
    validate(userValidation.getUser),
    logRoute('GET', '/users/:userId'),
    userController.getUser
  )
  .patch(
    auth('manageUsers'), // Permission admin requise ou accès personnel
    validate(userValidation.updateUser),
    logRoute('PATCH', '/users/:userId'),
    userController.updateUser
  )
  .delete(
    auth('manageUsers'), // Permission admin requise
    validate(userValidation.deleteUser),
    logRoute('DELETE', '/users/:userId'),
    userController.deleteUser
  );

module.exports = router;

